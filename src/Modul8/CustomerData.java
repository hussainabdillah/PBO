package Modul8;

public class CustomerData {
    String nama;
    String alamat;
    String tanggalLahir;
    String pekerjaan;
    int gaji;

    public CustomerData(){}
    public CustomerData(String nama, String alamat, String tanggalLahir, String pekerjaan, int gaji) {
        this.nama = nama;
        this.alamat = alamat;
        this.tanggalLahir = tanggalLahir;
        this.pekerjaan = pekerjaan;
        this.gaji = gaji;
    }
}
